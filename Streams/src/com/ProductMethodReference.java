package com;

//Method Reference in stream


public class ProductMethodReference {
	
	    public int id;  
	    public String name;  
	    public float price;  
	      
	    public ProductMethodReference(int id, String name, float price) {  
	        this.id = id;  
	        this.name = name;  
	        this.price = price;  
	    }  
	          
	    public int getId() {  
	        return id;  
	    }  
	    public String getName() {  
	        return name;  
	    }  
	    public float getPrice() {  
	        return price;  
	    }  

}
