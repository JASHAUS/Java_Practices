package com;

//Java Stream Example : Convert List into Set

public class ProductStreamSet {
	
	    public int id;  
	    public String name;  
	    public float price;  
	    public ProductStreamSet(int id, String name, float price) {  
	        this.id = id;  
	        this.name = name;  
	        this.price = price;  
	    }  

}
